// 辅助函数：根据ID查找商品
export function getProductById(spuId) {
    for (const group of ethnicGoodsList) {
        const found = group.items.find(item => item.id === spuId);
        if (found) return found;
    }
    return null;
}

// 辅助函数：获取所有商品
export function getAllProducts() {
    let all = [];
    ethnicGoodsList.forEach(group => {
        all = all.concat(group.items);
    });
    return all;
}
